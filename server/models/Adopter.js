const mongoose = require('mongoose')

const adopterSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
    trim: true,
  },
  phone: {
    type: String,
    required: true,
    trim: true,
  },
  address: {
    street: {
      type: String,
      required: true,
      trim: true,
    },
    city: {
      type: String,
      required: true,
      trim: true,
    },
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
})

const Adopter = mongoose.model('Adopter', adopterSchema)
module.exports = Adopter
